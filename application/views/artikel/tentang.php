 <h5 class="text-center" style="font-weight:bold">
   Kelompok Studi Linux - Universitas Negeri Gorontalo (KSL-UNG) mulai dirintis pada tanggal 20 November 2010 oleh para mahasiswa/i Universitas Negeri Gorontalo yang memiliki ketertarikan yang sama dibidang Teknologi Informasi lebih tepatnya pada Linux dan Open Source. <br> <br>
   Sebagai bukti kepedulian KSL-UNG dalam pemberdayaan masyarakat terhadap penggunakan Teknologi Informasi dan Komunikasi, KSL-UNG gencar melaksakan sosialisasi tentang Teknologi Informasi dan Komunikasi lebih tepatnya pada Linux dan Source kepada masyarakat luas dengan tujuan agar masyarakat dapat lebih mengenal dunia Teknologi Informasi dan Komunikasi dan menggunakan Software Open Source.
   KSL-UNG adalah komunitas yang berbasis pada Universitas Negeri Gorontalo yang bersifat nir-laba, independen, mandiri yang berdasar pada upaya pengembangan pengetahuan ketrampilan / ilmu pengetahuan di bidang Teknologi Informasi dan komunikasi bagi para anggota serta warga masyarakat lain. <br> <br>
   KSL-UNG resmi berdiri pada tanggal 20 November 2010 dan yang menjadi Ketua Umum pertama adalah Yunus Monoarfa, seiring berjalannya waktu, KSL-UNG sudah mengalami beberapa kali reorganisasi ulang mulai dari Ketua Umum pertama yaitu pengurus 2010 kemudian dilanjutkan oleh Pengurus 2011 di ketuai oleh Sutrisno Nusi, kemudian dilanjutkan oleh Pengurus 2012 di ketuai oleh Bachrudin Dai, kemudian dilanjutkan oleh Pengurus 2013 di ketuai oleh Ardiansyah Arsad & Moch. Nazar Ma'mun, kemudian dilanjutkan oleh Pengurus 2014-2016 di ketuai oleh Taufiq Hippy, kemudian dilanjutkan oleh Pengurus 2017 di ketuai oleh Fadlan Zainudhin, kemudian dilanjutkan oleh Pengurus 2018 di ketuai oleh Adnan Kasim. <br>
